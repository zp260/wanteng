#import "UIColor+Extension.h"

@implementation UIColor (Extension)

+ (UIColor *)xrBlueColor
{
    return UIColorFromRGB(0x74c8e6);
}

+ (UIColor *)xrBlackColor
{
    return UIColorFromRGB(0x303030);
}

+ (UIColor *)xrNaviColor
{
    return UIColorFromRGB(0x72d8dc/*00a9e4*/);
}

@end
